package Variables;

public class booleanStudy {

	public static void main(String[] args) {
		// I want to store my result -->pass/fail
		//pass-->true , pass-->false
			
		boolean result;
		result = true;
		System.out.println("My passing result is "+result);
		
		// I want to store switch condition -->On/OFF
		//On-->true , OFF-->false
		
		boolean switchCondition;
		switchCondition = false;
		System.out.println("Switch condition is "+switchCondition);

	}

}
