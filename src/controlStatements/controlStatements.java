package controlStatements;

public class controlStatements {
	public static void main(String[] args) {

		int a = 1000;
		int b = 200;

		System.out.println(a < b);
		System.out.println(a <= b);
		System.out.println(a > b);
		System.out.println(a >= b);
		System.out.println(a == b);
		System.out.println(a != b);

	}
}
